    @SuppressWarnings("empty-statement")
    private void btn_hinzufügenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hinzufügenActionPerformed
	try {
	    String gew;
	    String idString = null;
	    
	    gew = cbox_friends.getSelectedItem().toString();
	    String[] splitResult = gew.split(" ");
	    Calendar calendar = Calendar.getInstance();
	    java.util.Date utilDate = calendar.getTime();
	    java.sql.Date currentDate = new java.sql.Date(utilDate.getTime());
		Betrag = (double) sp_betrag.getValue();
	    
	    
	    MainMenu.SQL = "SELECT id FROM patchowe_personen WHERE vorname = ? AND nachname = ?";
	    MainMenu.prepareStatement();
	    MainMenu.stmt.setString(1, splitResult[0]);
	    MainMenu.stmt.setString(2, splitResult[1]);
	    MainMenu.rs = MainMenu.stmt.executeQuery();
	    
	    if (MainMenu.rs.next()) {
		idString=MainMenu.rs.getString(1);
	    }
	    
	    MainMenu.SQL = "INSERT INTO patchowe_transaktionen (pid, datum, betrag, beschreibung) VALUES ( ?, ?, ?, ?)";
        MainMenu.prepareStatement();
        MainMenu.stmt.setInt(1, Integer.parseInt(idString));
        MainMenu.stmt.setDate(2, currentDate);
        if (rdio_schuldet.isSelected()) {MainMenu.stmt.setDouble(3, (double) sp_betrag.getValue());} else {MainMenu.stmt.setDouble(3, -Betrag);}
		MainMenu.stmt.setString(4, txt_beschreibung.getText());
        MainMenu.stmt.execute();
		MainMenu.stmt.closeOnCompletion();
		txt_beschreibung.setText("");
		sp_betrag.setValue(0);
	    
	    this.infoBox("Der Schuldeneintrag für " + cbox_friends.getSelectedItem().toString() + " wurde erfolgreich erstellt!",
		    "Eintragung erfolgreich");
	    
	} catch (SQLException ex) {
	    this.errorBox("Ein Fehler ist aufgetreten.\nFehlermeldung:\n'" + ex.getMessage() + "'", "Fehler!");
	}
    }//GEN-LAST:event_btn_hinzufügenActionPerformed

