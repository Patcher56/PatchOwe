    protected void cboxrefresh() {
		try {
			MainMenu.SQL = "SELECT vorname, nachname FROM patchowe_personen";
			MainMenu.prepareStatement();
			MainMenu.rs = MainMenu.stmt.executeQuery();
			cbox_friends.removeAllItems();
			while (MainMenu.rs.next()) {
				cbox_friends.addItem(MainMenu.rs.getString("vorname") + " " + MainMenu.rs.getString("nachname"));
			}
			if (si_friends >= 0) {
				cbox_friends.setSelectedIndex(si_friends);
			}
		} catch (SQLException ex) {
			this.errorBox("Ein Fehler ist aufgetreten.\nFehlermeldung:\n'" + ex.getMessage() + "'", "Fehler!");
		}
    }
    
