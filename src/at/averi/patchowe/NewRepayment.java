    private void btn_begleichenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_begleichenActionPerformed
        if (chk_teilrückzahlung.isSelected() == false) {
			try {
				MainMenu.SQL = "DELETE FROM patchowe_transaktionen WHERE id = ?";
				MainMenu.prepareStatement();
				MainMenu.stmt.setInt(1, SelectedID);
				MainMenu.stmt.execute();
				
				MainMenu.infoBox(this, "Der Schuldeneintrag für " + txt_name.getText() + " in der Höhe von " + txt_betrag.getText()
						+ " wurde erfolgreich beglichen!", "Erfolgreich beglichen");
				
			} catch (SQLException ex) {
				MainMenu.errorBox(this, "Ein Fehler ist aufgetreten.\nFehlermeldung:\n'" + ex.getMessage() + "'", "Fehler!");
			}
		}
		else {
			try {
				MainMenu.SQL = "UPDATE patchowe_transaktionen SET betrag = betrag - ? WHERE id = ?";
				MainMenu.prepareStatement();
				MainMenu.stmt.setDouble(1, (double) sp_betrag.getValue());
				MainMenu.stmt.setInt(2, SelectedID);
				MainMenu.stmt.execute();
				MainMenu.infoBox(this, "Die Teilrückzahlung für " + txt_name.getText() + " in der Höhe von " + f.format(sp_betrag.getValue())
						+ " wurde erfolgreich durchgeführt!", "Erfolgreich durchgeführt!");
			} catch (SQLException ex) {
				Logger.getLogger(NewRepayment.class.getName()).log(Level.SEVERE, null, ex);
			}
		}
    }//GEN-LAST:event_btn_begleichenActionPerformed

