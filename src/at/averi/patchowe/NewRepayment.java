	private void namesrefresh() {
		try {
			MainMenu.SQL = "SELECT vorname, nachname FROM patchowe_personen";
			MainMenu.prepareStatement();
			MainMenu.rs = MainMenu.stmt.executeQuery();
			cbox_freund.removeAllItems();
			while (MainMenu.rs.next()) {
				cbox_freund.addItem(MainMenu.rs.getString("vorname") + " " + MainMenu.rs.getString("nachname"));
			}
			//if (si_friends >= 0) {
				//cbox_freund.setSelectedIndex(si_friends);
			//}
		} catch (SQLException ex) {
			MainMenu.errorBox(this, "Ein Fehler ist aufgetreten.\nFehlermeldung:\n'" + ex.getMessage() + "'", "Fehler!");
		}
	}
	
	private void transrefresh() {
		try {
			String sel;
			
			sel = cbox_freund.getSelectedItem().toString();
			
			String[] splitResult = sel.split(" ");
			
			String idString = null;
			
			MainMenu.SQL = "SELECT id FROM patchowe_personen WHERE vorname = ? AND nachname = ?";
			MainMenu.prepareStatement();
			MainMenu.stmt.setString(1, splitResult[0]);
			MainMenu.stmt.setString(2, splitResult[1]);
			rstrans = MainMenu.stmt.executeQuery();
			
			if (rstrans.next()) {
				idString=rstrans.getString(1);
			}
			
			
			MainMenu.SQL = "SELECT id, beschreibung, betrag "
					+ "FROM patchowe_transaktionen WHERE pid = ?";
			MainMenu.prepareStatement();
			MainMenu.stmt.setInt(1, Integer.parseInt(idString));
			rstrans = MainMenu.stmt.executeQuery();
			cbox_transaktion.removeAllItems();
			while (rstrans.next()) {
				cbox_transaktion.addItem(rstrans.getInt("id") + " - " + rstrans.getString("beschreibung")
						+ " - " + f.format(rstrans.getDouble("betrag")));
			}
			//if (si_friends >= 0) {
				//cbox_freund.setSelectedIndex(si_friends);
			//}
		} catch (SQLException ex) {
			MainMenu.errorBox(this, "Ein Fehler ist aufgetreten.\nFehlermeldung:\n'" + ex.getMessage() + "'", "Fehler!");
		}
	}
	
	private void inforefresh() {
		try {
			String sel;
			sel = cbox_transaktion.getSelectedItem().toString();
			String[] splitResult = sel.split(" - ");
			double Betrag;
			SelectedID = Integer.parseInt(splitResult[0]);
			
			MainMenu.SQL = "SELECT betrag, beschreibung, datum FROM patchowe_transaktionen WHERE id = ?";
			MainMenu.prepareStatement();
			MainMenu.stmt.setInt(1, SelectedID);
			rsinfo = MainMenu.stmt.executeQuery();
			if (rsinfo.next()) {
				Betrag = rsinfo.getDouble("betrag");
				
				if (Betrag >= 0) {
					rdio_verliehen.setSelected(true);
				}
				else {
					rdio_schuld.setSelected(true);
				}
				txt_betrag.setText(f.format(Math.abs(Betrag)));
				txt_beschreibung.setText(rsinfo.getString("beschreibung"));
				txt_datum.setText(d.format(rsinfo.getDate("datum")));
			}
			
		} catch (SQLException ex) {
			Logger.getLogger(NewRepayment.class.getName()).log(Level.SEVERE, null, ex);
		}
	}
